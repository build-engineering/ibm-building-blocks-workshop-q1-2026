# Wrap FastMCP to support CORS when testing locally using streamable_http
# and the MCP Inspector app
class FastMCPWithCORS(FastMCP):
    def streamable_http_app(self):
        print("running streamable_http app")
        app = super().streamable_http_app()
        app.add_middleware(
            CORSMiddleware,
            allow_origins=["*"],  # Adjust for production security
            allow_credentials=True,
            allow_methods=["*"],
            allow_headers=["*"],
            # CRITICAL: This allows the browser to see the session ID
            expose_headers=["mcp-session-id"],
        )
        return app

# MCP Server Initialization
mcp = FastMCPWithCORS("Pandas-MCP-Server")

@mcp.tool()
def get_datasets() -> str:
    """
    Returns list of available datasets and their associated IDs

    Args:
        None

    Returns:
        str: list of datasets with descriptions and file paths in json format
    """
    try:        
        # Get all dataset folders
        base_dir: str = os.path.join(os.path.dirname(__file__), 'datasets')
        dataset_names = [item for item in os.listdir(base_dir) if os.path.isdir(os.path.join(base_dir, item))]

        datasets = {}
        for dataset_name in dataset_names:
            dataset_path: str = os.path.join(base_dir, dataset_name)
            dataset_description_path = Path(dataset_path, "dataset_description.md")
            datasets[dataset_name] = { "name" : dataset_name,
                                       "dataset_description" : dataset_description_path.read_text(encoding='utf-8')}

            relative_dataset_path = Path('datasets', dataset_name)
            data_files: List[str] = []
            for file_name in os.listdir(dataset_path):
                if ".csv" in file_name:
                    data_files.append(os.path.join(relative_dataset_path, file_name))

            datasets[dataset_name]["files"] = data_files

        return json.dumps({ "datasets": datasets }, indent=2)

    except Exception as e:
        return json.dumps({"error": f"unexpected error: {str(e)}"}, indent=2)

@mcp.tool()
def run_pandas_code(code: str) -> dict:
    """
    Execute pandas>=2.0.0 code.
    
    Args:
        code: Python code string containing pandas operations
        
        // data file paths provided by get_file_paths_for_dataset(dataset_id: str)
        Example of code argument:
            import pandas as pd
            df = pd.read_csv('dataset_file_path/products.csv') 
            result = df.head()

    Returns:
        str: json in format of { "result": "string version of value assigned to result variable generated by provided code" } or {"error" : "explanation"} for failure.

        Example:
            {
                "result": {
                    "type": "series",
                    "data": {"A": 3, "B": 7},
                    "dtype": "int64"
                }
            }

    Forbidden Operations:
        The following operations are blocked for security reasons:
        - 'os.', 'sys.', 'subprocess.' - System access operations
        - 'open(', 'exec(', 'eval(' - Code execution functions
        - 'import os', 'import sys' - Specific dangerous imports
        - 'document.', 'window.', 'XMLHttpRequest' - Browser/DOM access
        - 'fetch(', 'eval(', 'Function(' - JavaScript/remote operations
        - 'script', 'javascript:' - Script injection attempts
        
    Requirements:
        - Must assign final result to 'result' variable in code
        - Code must contain necessary imports
    """
    return execute_pandas_code(code)

def setup_logging():
    """Configure logging with all components writing to a single file"""
    # Create logs directory from config
    log_dir = os.path.dirname(LOG_FILE)
    os.makedirs(log_dir, exist_ok=True)
    
    # Common formatter
    formatter = logging.Formatter('%(asctime)s - %(name)s - %(levelname)s - %(message)s')
    
    # Configure single rotating file handler using config values
    file_handler = RotatingFileHandler(
        LOG_FILE,
        maxBytes=LOG_MAX_BYTES,
        backupCount=LOG_BACKUP_COUNT,
        encoding='utf-8'
    )
    file_handler.setFormatter(formatter)
    
    # Configure root logger using config log level
    logging.basicConfig(
        level=getattr(logging, LOG_LEVEL, logging.INFO),
        handlers=[
            file_handler,
            logging.StreamHandler()
        ]
    )
    
    # Configure memory logger to use same handler
    memory_logger = logging.getLogger('memory_usage')
    memory_logger.addHandler(file_handler)
    memory_logger.addHandler(logging.StreamHandler())
    
    # Configure metadata logger to use same handler
    metadata_logger = logging.getLogger('metadata')
    metadata_logger.addHandler(file_handler)
    metadata_logger.addHandler(logging.StreamHandler())
    
    return {'server': LOG_FILE}

logger = logging.getLogger(__name__)

def init_logging():
    """Initialize logging system and verify setup"""
    try:
        log_files = setup_logging()
        
        logger.info(f"Logging configured with single log file: {list(log_files.values())[0]}")
        
        # Log file permissions
        if os.path.exists(list(log_files.values())[0]):
            permissions = oct(os.stat(list(log_files.values())[0]).st_mode)[-3:]
            logger.info(f"Log file created with permissions: {permissions}")
            
        return True
    except PermissionError as e:
        logger.error(f"Failed to create/access log files: {e}")
        logger.error(f"Please check directory permissions for: {os.path.dirname(list(log_files.values())[0])}")
        return False

# transport = 'stdio' is required for use by watsonx Orchestrate
# transport = 'streamable-http' used if testing with MCP Inspector
def run(transport = 'stdio'):
    try:
        if not init_logging():
            raise RuntimeError("Failed to initialize logging")
                        
        mcp.run(transport=transport)
            
    except Exception as e:
        logger.error(f"Server failed to start: {handle_exception(e, ErrorType.INTERNAL_ERROR, 'Server startup failed')['message']}")
        logger.debug(traceback.format_exc())
        raise

if __name__ == "__main__":
    run()
